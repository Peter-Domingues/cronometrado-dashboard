import React, { HTMLProps } from "react";

import "./ClientsTable.styles.css";

import {
  Column,
  ColumnDef,
  flexRender,
  getCoreRowModel,
  getFilteredRowModel,
  getPaginationRowModel,
  Table,
  useReactTable,
} from "@tanstack/react-table";
import { makeData, Person } from "../makeData";
import { formatDateToDDMMYYYY } from "@/helpers/format";

